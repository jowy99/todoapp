export const messages: Record<AppLocale, MessageTable> = { en, es };

function interpolateMessage(template: string, values?: MessageValues) {
  if (!values) {
    return template;
  }

  return template.replace(/\{([\w.]+)\}/g, (_, key: string) => {
    const value = values[key];
    return value === undefined || value === null ? "" : String(value);
  });
}

export function getMessage(locale: AppLocale, key: MessageKey, values?: MessageValues): string {
  const localized = messages[locale][key] ?? messages.en[key];
  return interpolateMessage(localized, values);
}
